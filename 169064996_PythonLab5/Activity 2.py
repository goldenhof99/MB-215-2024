# Define a simple list with three elements
simple_list = ['a', 'b', 'c']

# Use the repetition operator (*) to make three copies of the list
repeated_list = simple_list * 3  # This repeats the list three times

# Print the new list created by the repetition operator
print("Repeated list:", repeated_list)

# Iterate over the repeated list using a for loop and print each element
for element in repeated_list:
    print(element)
