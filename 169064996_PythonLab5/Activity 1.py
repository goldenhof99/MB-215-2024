# Define a list with different data types
my_list = [1, "two", 3.0, True]

# Print the entire list
print("The entire list:", my_list)

# Define a tuple with different data types
my_tuple = (4, "five", 6.0, False)

# Convert the tuple to a list using the list() function
tuple_to_list = list(my_tuple)

# Print the converted list
print("Converted list from tuple:", tuple_to_list)
